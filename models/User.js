const { Schema, model } = require("mongoose");

//Schema to create User model
const userSchema = new Schema({
  username: {
    type: String,
    required: true,
    unique: true,
    trim: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
    trim: true,
    match: /^[^\s@]+@[^\s@]+\.[^\s@]+$/,
  },
  thoughts: [
    {
      type: mongoose.Schema.Types.ObjectId,
      ref: "Thought",
    },
  ],
  friends: [
    {
      type: mongoose.Schema.Types.ObjectId,
      ref: "User",
    },
  ],
});

//Creates a virtual property for friendCount
userSchema.virtual("friendaCount").get(function () {
  return this.friends.length;
});

const User = model("user", userSchema);

module.exports = User;
